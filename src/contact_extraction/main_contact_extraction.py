    base_output = args.output.resolve() if args.output else (path_CT.parent / "out")
    dirs = build_output_dirs(base_output)

    output_dir                  = dirs["output_dir"]
    output_dir_coregistration   = dirs["output_dir_coregistration"]
    output_dir_input_ElectroLoc = dirs["output_dir_input_ElectroLoc"]
    output_dir_mask             = dirs["output_dir_mask"]
    output_csv_ElectroLoc       = dirs["output_csv_ElectroLoc"]

    MNI_path, AAL_path, AAL_txt_path, brain_mask_MNI_path = resolve_atlas_paths(args)

