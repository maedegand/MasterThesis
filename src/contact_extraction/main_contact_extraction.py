            "  python main.py CT.nii.gz MRI.nii.gz brain_mask_CT.nii.gz iEEG.edf\n\n"
            "With custom output dir and ElectroLoc:\n"
            "  python main.py CT.nii.gz MRI.nii.gz brain_mask_CT.nii.gz iEEG.edf\n"
            "      -o ./results  --electroloc path/to/MasterThesis_ElectroLoc\n\n"
            "With custom atlas:\n"
            "  python main.py CT.nii.gz MRI.nii.gz brain_mask_CT.nii.gz iEEG.edf\n"
            "      --mni MNI.nii.gz --atlas aal.nii.gz --atlas-txt AAL_atlas.txt\n"
            "      --brain-mask-mni brain_mask_mni.nii.gz\n\n"
            "Note: electrode_info CSV files are entered interactively at runtime\n"
            "(they are generated by ElecTool during the preprocessing step)."
